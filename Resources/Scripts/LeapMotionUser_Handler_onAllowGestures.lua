function LeapMotionUser.onAllowGestures ( bSwipe, bCircle, bScreentap, bKeytap )
--------------------------------------------------------------------------------
	
	if leapmotion.allowGestures ( bSwipe, bCircle, bScreentap, bKeytap ) == false then
        log.message ( "LEAP ShiVa: Setting gestures failed." )
    end
	
--------------------------------------------------------------------------------
end
